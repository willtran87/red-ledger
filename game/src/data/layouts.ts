const normalizeFootprint = (rows: readonly string[]): readonly string[] => rows.map((row) => {
  if (row.length === 21) return row;
  if (row[0] !== '#' || row.at(-1) !== '#' || row.length < 21) {
    throw new Error(`Invalid authored layout row ${JSON.stringify(row)}`);
  }
  // Preserve the sealed boundary while trimming surplus drafting cells from
  // the right edge of the 19-cell playable span.
  return `#${row.slice(1, -1).slice(0, 19)}#`;
});

export const getLayout = (id: LayoutId, episode: EpisodeNumber): readonly string[] =>
  normalizeFootprint(layouts[episode][id]);
