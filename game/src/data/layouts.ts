export const validateAuthoredFootprint = (rows: readonly string[]): readonly string[] => {
  if (rows.length !== 15) throw new Error(`Invalid authored layout height ${rows.length}; expected 15`);
  rows.forEach((row, index) => {
    if (row.length !== 21) throw new Error(`Invalid authored layout row ${index}: width ${row.length}; expected 21`);
    if (row[0] !== '#' || row.at(-1) !== '#') throw new Error(`Authored layout row ${index} has an open boundary`);
  });
  return rows;
};

export const getLayout = (id: LayoutId, episode: EpisodeNumber): readonly string[] =>
  validateAuthoredFootprint(layouts[episode][id]);
